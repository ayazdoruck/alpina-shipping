function setLanguage(lang) {
    localStorage.setItem('alpina_lang', lang);
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });

    // Update placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[lang][key]) {
            el.setAttribute('placeholder', translations[lang][key]);
        }
    });

    // Update Lang Switcher UI
    const langBtn = document.querySelector('.lang-switch');
    if (langBtn) {
        langBtn.innerHTML = `<i data-lucide="globe"></i> ${lang === 'tr' ? 'EN' : 'TR'}`;
        if (window.lucide) lucide.createIcons();
    }
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('alpina_lang') || 'tr';
    setLanguage(savedLang);

    const langBtn = document.querySelector('.lang-switch');
    if (langBtn) {
        langBtn.addEventListener('click', () => {
            const currentLang = localStorage.getItem('alpina_lang') || 'tr';
            setLanguage(currentLang === 'tr' ? 'en' : 'tr');
        });
    }
});
